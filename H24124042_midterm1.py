    #列印每3個數的99乘法表
    while j>=1:#列印1到9，所以j>=1
        print(j,"X",i,"=",i*j,end="\t")#印完後不換行且空格 \t
        print(j,"X",(i-1),"=",(i-1)*j,end="\t")
        print(j,"X",(i-2),"=",(i-2)*j)
        j=j-1
    print()#執行完3個數字的乘積從1到9後換行
